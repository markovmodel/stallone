    /**
     * Test of evd method, of class AlgebraUtilities.
     */
    @Test
    public void testEvd_3args()
    {
        System.out.println("evd");
        
        IDoubleArray T = Doubles.create.array(new double[][]{
            {0.9, 0.1, 0},
            {0.1, 0.8, 0.1},
            {0, 0.1, 0.9}
        });

        double[][] Rref = new double[][]{
            {0.5773502691896264, 0.7071067811865478, 0.4082482904638628}, 
            {0.5773502691896255, -4.777636852870778E-16, -0.8164965809277261},
            {0.5773502691896255, -0.7071067811865472, 0.40824829046386324 }
        };

        double[][] Lref = new double[][]{
            {0.5773502691896258, 0.5773502691896258, 0.5773502691896258},
            {0.7071067811865474, -5.707691561796544E-16, -0.7071067811865477},
            {0.4082482904638623, -0.8164965809277264, 0.4082482904638632}
        };
        
        IEigenvalueDecomposition evd = Algebra.util.evd(T, true, true);

        assertTrue(Algebra.util.numericallyEquals(evd.getEval(),
                Doubles.create.arrayFrom(1, 0.9, 0.7), 1e-7));
        assertTrue(Algebra.util.numericallyEquals(evd.R(),
                Doubles.create.array(Rref), 1e-7));
        assertTrue(Algebra.util.numericallyEquals(evd.L(),
                Doubles.create.array(Lref), 1e-7));
    }

